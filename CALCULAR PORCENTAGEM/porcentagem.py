valor = float(input('Qual o valor que você quer calcular a porcentagem? '))
porcentagem = float(input('Quantos porcento você quer calcular? '))
valor_da_porcentagem = porcentagem/100
total_porcentagem = valor_da_porcentagem*valor
print(f'{porcentagem} % de {valor} é igual á: {total_porcentagem}')
